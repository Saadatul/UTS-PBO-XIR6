    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        // TODO add your handling code here:
       int baris = tabel1.getSelectedRow();
       if (baris != -1){
           String Nama = tabel1.getValueAt(baris, 0).toString();
           String SQL = "DELETE FROM tabel_transaksi WHERE nama='"+Nama+"'";
           int status = KoneksiDB.execute(SQL);
           if (status==1){
               JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
           } else {
               JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
           }
       } else {
           JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_bDeleteActionPerformed

    private void bTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTotalActionPerformed
        // TODO add your handling code here:
        
         int mak = Integer.parseInt(jMakan.getText());
         int mik = Integer.parseInt(jMinum.getText());
        
        int Jumlah = mak + mik;

        int SubTotal = (Jumlah);
        String iSubTotal = String.valueOf(SubTotal);
        total.setText(iSubTotal);
        
        int bay = Integer.parseInt(bayar.getText());
        int tot = Integer.parseInt(total.getText());
        
        int Kembali = bay - tot;
        
        int kembalian = (Kembali);
        String iKembali = String.valueOf(kembalian);
        kembali.setText(iKembali);
    
        if (SubTotal > 200000){
            int diskon = (int) (SubTotal*10/100);
            total.setText(String.valueOf(SubTotal-diskon));
        } else 
            total.setText(String.valueOf(SubTotal));
    }//GEN-LAST:event_bTotalActionPerformed

