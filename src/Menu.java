    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        // TODO add your handling code here:
        if ("".equals(nama.getText()) || "".equals(nomeja.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi data ", "Error", 
                    JOptionPane.WARNING_MESSAGE);
        } else {
            int makan = Integer.parseInt(String.valueOf(jMakan.getText()));
            int minum = Integer.parseInt(String.valueOf(jMinum.getText()));
            int totalll = makan + minum;
            
            total.setText(String.valueOf(totalll));
        
            
            String SQL = "INSERT INTO tabel_transaksi (nama,no_meja,makanan,minuman,total,bayar,kembali) " 
                    + "VALUES ('"+nama.getText()+"','"+nomeja.getText()+"','"+jMakan.getText()+"','"+jMinum.getText()+"','"+total.getText()+"','"+bayar.getText()
                    +"','"+kembali.getText()+"')";
            int status = KoneksiDB.execute(SQL); 
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
              
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
             
        }
    }//GEN-LAST:event_bSaveActionPerformed

