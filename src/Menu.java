    private void selectData() {
       String kolom[] = {"Nama","NoMeja","Makanan","Minuman","SubTotal","Bayar","Kembali"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tabel_transaksi";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String Nama = rs.getString(1);
                String NoMeja = rs.getString(2);
                String Makanan = rs.getString(3);
                String Minuman = rs.getString(4);
                String SubTotal = rs.getString(5);
                String Bayar = rs.getString(6);
                String Kembali = rs.getString(7);
                
                String data[] = {Nama,NoMeja,Makanan,Minuman,SubTotal,Bayar,Kembali};
                dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel1.setModel(dtm);
    }
    
